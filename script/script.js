const web3 = new Web3(DEVETH_RPC)
const dinuContract = new web3.eth.Contract(CONTRACT_ABI, CONTRACT_ADDRESS)

async function getStats() {
	dinuContract.methods.totalSupply().call(function (error, result) {
		if (result) {
			renderTotalsupply(result);
		} else {
			console.error("No network result!", error);
		}
	});

	dinuContract.methods.getDevs().call(function (error, result) {
		if (result) {
			renderDevsCounter(result);
		} else {
			console.error("No network result!", error);
		}
	});
}
